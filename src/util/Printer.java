package util;

public class Printer {

    public static void print(String str) {
        System.out.println(str);
    }

    public static void print(int i) {
        System.out.println(i);
    }

    public static void print(double i) {
        System.out.println(i);
    }

    public static void print(long i) {
        System.out.println(i);
    }
}
